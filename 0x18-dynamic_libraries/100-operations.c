/**
 * add - add two numbers
 * @x: num1
 * @y: num2
 * Return: added numbers
 */
int add(int x, int y)
{
	return (x + y);
}

/**
 * sub - subtract two numbers
 * @x: number 1
 * @y: number 2
 * Return: difference of numbers
 */
int sub(int x, int y)
{
	return (x - y);
}

/**
 * mul - multiply two numbers
 * @y: number 1
 * @x: number 2
 * Return: product of two numbers
 */
int mul(int x, int y)
{
	return (x * y);
}

/**
 * div - divide two numbers
 * @a: num1
 * @b: num2
 * Return: quotient of two numbers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulo two numbers
 * @a: num1
 * @b: num2
 * Return: modulouseous of two numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
